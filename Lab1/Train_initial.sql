DROP DATABASE IF EXISTS Train;
CREATE DATABASE Train;
USE Train;

SET FOREIGN_KEY_CHECKS=0;

CREATE TABLE CrewMember (
  crewId INT UNSIGNED NOT NULL AUTO_INCREMENT,
  lastName VARCHAR(45) NOT NULL,
  firstName VARCHAR(45) NOT NULL,
  supervisor INT UNSIGNED, 
  PRIMARY KEY (crewId),
  FOREIGN KEY (supervisor) REFERENCES CrewMember (crewId)
);

CREATE TABLE LocationMarker (
  locationId INT UNSIGNED NOT NULL AUTO_INCREMENT,
  xLocation INT DEFAULT NULL,
  yLocation INT DEFAULT NULL,
  PRIMARY KEY (locationId)
);

CREATE TABLE Train (
  trainId INT UNSIGNED NOT NULL AUTO_INCREMENT,
  departDate DATETIME NOT NULL,
  departMarker INT UNSIGNED,
  scheduledArrival DATETIME NOT NULL,
  actualArrival DATETIME DEFAULT NULL,
  arrivalMarker INT UNSIGNED,
  PRIMARY KEY (trainId),
  FOREIGN KEY (departMarker) REFERENCES LocationMarker (locationId),
  FOREIGN KEY (arrivalMarker) REFERENCES LocationMarker (locationId)
);

CREATE TABLE CrewToTrain (
  trainId INT UNSIGNED NOT NULL,
  crewId INT UNSIGNED NOT NULL,
  shift CHAR(15),
  PRIMARY KEY (trainId,crewId),
  FOREIGN KEY (trainId) REFERENCES Train (trainId),
  FOREIGN KEY (crewId) REFERENCES CrewMember (crewId)
);

CREATE TABLE TrainCar (
  carId INT UNSIGNED NOT NULL AUTO_INCREMENT,
  manufacturer VARCHAR(60) NOT NULL,
  yearBuilt INT UNSIGNED NOT NULL,
  PRIMARY KEY (carId)
);

CREATE TABLE EngineCar (
  carId INT UNSIGNED NOT NULL,
  horsePower INT UNSIGNED NOT NULL,
  dateLastEngineService DATE NOT NULL,
  PRIMARY KEY (carId),
  FOREIGN KEY (carId) REFERENCES TrainCar (carId)
);

CREATE TABLE CarInTrain (
  carId INT UNSIGNED NOT NULL,
  trainId INT UNSIGNED NOT NULL,
  carPosition INT UNSIGNED,
  PRIMARY KEY (carId,trainId)
);

CREATE TABLE Route (
  trainId INT UNSIGNED NOT NULL,
  locationId INT UNSIGNED NOT NULL,
  routePosition INT UNSIGNED,
  PRIMARY KEY (trainId,locationId),
  FOREIGN KEY (trainId) REFERENCES Train (trainId),
  FOREIGN KEY (locationId) REFERENCES LocationMarker (locationId)
);

